print('hello\nworld') # \n表示换行
print('hello\twirld') # \t表示横向制表符，其实就是多个空格
print('he\bllo') # \b表示退格，就是光标回退一格

print('let\'s go') # \'表示普通的单引号，不再是字符串的表示符号
print("小明说:\"你好\"") # \"表示普通的双引号，不再是字符串的表示符号

print('C:\\test\\poptest\\name.txt') # \\表示普通的反斜杠
print(r'C:\test\poptest\name.txt') # r表示原始字符串，不是万能的